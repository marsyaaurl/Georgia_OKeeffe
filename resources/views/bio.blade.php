                    <div class="bio-section">
                    <p>
                        Born on November 15, 1887, the second of seven children, <b>Georgia Totto O'Keeffe</b> grew up on a farm near Sun Prairie, Wisconsin. 
                    As a child she received art lessons at home. Her abilities were recognized and encouraged by teachers throughout her school years. 
                    By the time she graduated from high school in 1905, O'Keeffe had determined to make her way as an artist. O'Keeffe studied at the Art Institute of Chicago (1905-1906) and the Art Students League in New York (1907-1908), where she learned the techniques of traditional realist painting. 
                    In 1908, she won the League's William Merritt Chase still-life prize for her oil painting <b>Untitled (Dead Rabbit with Copper Pot)</b>. 
                    </p><br>
                    <p>
                        The direction of her artistic practice shifted dramatically four years later (1912) when she took a summer course at the University of Virginia, taught by <b>Alon Bement</b> of Teachers College, Columbia University. 
                    Bement introduced her to the revolutionary ideas of his colleague Arthur Wesley Dow. Dow encouraged an intellectual and imaginative process of making art that was grounded in personal expression and harmonious design.
                    </p><br>
                    <p>
                        <i>“This man had one dominating idea,”</i> according to O'Keeffe, <i>“to fill a space in a beautiful way— and that interested me.”</i> Dow's approach offered O'Keeffe an alternative to realism, and she experimented with it for two years, while she taught art in the Amarillo, Texas public schools (1912-14) and worked summers in Virginia as Bement's assistant. 
                    O'Keeffe studied in New York from 1914 to 1915, at Teachers College, Columbia University and by the fall of 1915 she was teaching art at Columbia College, in South Carolina. She began a series of abstract charcoal drawings, to develop a personal language through which she could express her feelings and ideas.
                    ate in life she recalled, <i>“I realized that I had things in my head not like what I had been taught - not ike what I had seen - shapes and ideas so familiar to me that it hadn't occurred to me to put them down. 
                    I decided to stop painting, to put away everything I had done, and to start to say the things that were my own.”</i> She mailed some of these highly abstract drawings to a friend in New York City.
                    Her friend showed them to <b>Stieglitz</b>, the art dealer and renowned photographer, who would eventually become O'Keeffe's husband. He became the first to exhibit her work, in 1916.
                    </p>
