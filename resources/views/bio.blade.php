                    <div class="artistic-style-list">
                        <ul>
                            <li>
                                O'Keeffe loved the forms and shapes of nature. She would walk miles in the desert sun of New Mexico, collecting rocks and sun-bleached bones.
                            </li><br>
                            <li>
                                Many of the forms she paints are simplified, and sculptural, with gently rounded corners like the adobe houses of New Mexico where she lived for many years.
                            </li><br>
                            <li>
                                The positive and negative shapes in many of her paintings are simple and graphic. The shapes are easily distinguishable and clean, even in her paintings of the urban landscape of New York City, such as New York With Moon, (1925, 48"x30").
                            </li>
                            <div class="artistic-style-painting">
