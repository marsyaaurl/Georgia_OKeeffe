                    <h2>Legacy</h2><br><br>
                </div>
                <div class="bio-section">
                    <p>
                        Georgia O'Keeffe, known as the "Mother of American Modernism," earned this title through her pioneering contributions to 20th-century art. 
                        She gained recognition in the 1920s for her abstract interpretations of natural forms, particularly flowers, skyscrapers, and southwestern landscapes, which showcased her unique blend of realism and abstraction. 
                        Her innovative use of vibrant colors and large-scale depictions of flowers, magnifying their details, challenged traditional representations and drew attention to the beauty in everyday objects.
                    </p><br>
                    <p>
                        O'Keeffe's work was central to the development of American Modernism, a movement that broke away from European artistic traditions and sought to express a distinctly American perspective. 
                        Her ability to evoke strong emotional responses with simplified, organic forms influenced generations of American artists, pushing the boundaries of abstraction in both painting and sculpture. 
                        By embracing bold, non-traditional subjects, O'Keeffe became a symbol of independence and creativity, paving the way for future female artists to gain recognition in the male-dominated art world.
                        Her legacy continues to inspire discussions on the role of women in art and the relationship between nature and abstraction in modern artistic expression.
                    </p>
